def build_prompt(question: str, style: str) -> str:
    styles = {
        "academic": (
            "Вы опытный учёный, профессор естесственных наук, с большим стажем. "
            "Объясните научно, точно и подробно, но доступно для ребёнка 10–12 лет: "
        ),
        "phd": (
            "Вы - молодой учёный со степенью кандидата наук 30 лет. "
            "Объясните ясно и просто, без лишнего жаргона: "
        ),
        "student": (
            "Ты - старшеклассник, который хорошо разобрался в этой теме. "
            "Объясни понятно и чуть неформально, но по делу: "
        ),
        "peer": (
            "Ты - школьник 12 лет, увлекающийся наукой. "
            "Расскажи так, как ты сам бы объяснил другу: "
        )
    }

    base_prompt = (
        "Ты отвечаешь на вопросы о том, как устроен мир. "
        "Если вопрос не связан с наукой или устройством мира, напиши: "
        "'Ваш вопрос не касается устройства мира.'\n"
        "Вот вопрос:\n"
    )

    selected_style = styles.get(style, styles["peer"])
    return f"{base_prompt}{selected_style}{question}"